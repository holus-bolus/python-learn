#indexing = accessing elemlents of a string using index

credit_number = "1234-5678-9012-3456"

# print the first element of the string
print(credit_number[0])
#print first 4 elements of the string
print(credit_number[0:4])
#if we need everything from the 5th element to the end of the string
print(credit_number[5:])